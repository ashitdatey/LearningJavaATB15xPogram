package ATB15xTests_11th_Jan;

public class DoWhile1_10 {
    public static void main(String[] args) {
        //Write a Java program to print numbers from 1 to 10 using a do-while loop.
        int i = 1;
        do {
            System.out.println(i);
            i++;
        }while(i<=10);

    }
}
